#!/bin/bash
# 一次性生成全部旁白音频
# 使用 macOS Tingting 高品质中文女声

DIR="/Users/rachel/资料/渡世人 小说/概念验证/旁白音频"
mkdir -p "$DIR"

say -v Tingting "灰巢的太阳从来不像太阳。" -o "$DIR/01.aiff"
say -v Tingting "它是一颗衰老的红矮星，挂在垃圾行星的轨道外侧，每天懒洋洋地升起来，把整个废料场染成铁锈的颜色。" -o "$DIR/02.aiff"
say -v Tingting "如果你在灰巢住得够久，你会忘记什么叫正午。这里只有两种光——灰蒙蒙的日光，和霓虹灯管的冷光。" -o "$DIR/03.aiff"
say -v Tingting "灰。三十七岁。看起来像五十岁。" -o "$DIR/04.aiff"
say -v Tingting "联邦财产——她曾经是。A-7渡鸦型改造人，渡鸦小队的一号机。造价相当于一艘护卫舰。现在她蹲在废料场里修空调，一个小时收五个信用点。" -o "$DIR/05.aiff"
say -v Tingting "深夜。唯一的光源是她右眼的红色义眼。她对着镜子，用镊子修补左肩断口。镊子夹起一块烧焦的皮肤组织——她没有皱眉头。" -o "$DIR/06.aiff"
say -v Tingting "改造人的功能——记忆记录。每一次战斗，每一个任务，每一个指令都被完整地记录下来。关不掉。删除不了。像一台永远不会坏的摄像机，二十四小时对着你的脑子拍。" -o "$DIR/07.aiff"
say -v Tingting "这个梦她做了七年。七年，两千五百多个夜晚。每一次都一样——村庄、命令、倒计时、红色的错误提示。" -o "$DIR/08.aiff"
say -v Tingting "然后她注意到——房间角落里有一个正在消退的人形热源。地面上一小片湿痕。脚印。刚走不久。有人在她睡着的时候进来过。" -o "$DIR/09.aiff"
say -v Tingting "七年来，每个月都会有一个夜晚——有人会潜入她的房间。每次都停留不超过三十秒。什么都不拿走。什么都不留下。只留下一个脚印。" -o "$DIR/10.aiff"
say -v Tingting "她闭上一只眼睛。义眼不能闭——红色的光在黑暗中一明一灭。像一颗不会熄灭的、被困在铁皮盒子里的红星。" -o "$DIR/11.aiff"

echo "完成！音频文件在: $DIR"
echo "共生成 $(ls "$DIR" | wc -l) 个文件"
ls -la "$DIR"
